public class Items {

    public String teamone;
    public String teamtwo;
    public String series;
    public String venue;
    public String datetime;
    public String teamA;
    public String teamB;

    public Items(String teamone, String teamtwo, String series, String venue, String datetime, String teamA, String teamB) {
        this.teamone = teamone;
        this.teamtwo = teamtwo;
        this.series = series;
        this.venue = venue;
        this.datetime = datetime;
        this.teamA = teamA;
        this.teamB = teamB;
    }
}
